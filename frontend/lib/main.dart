import 'components/shared/glass_container.dart';
import 'bloc/theme/theme_cubit.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    // Initialize ApiService singleton
    final apiService = ApiService();

    return MultiBlocProvider(
      providers: [
        BlocProvider<AuthBloc>(
          create: (context) => AuthBloc()..add(CheckAuthStatus()),
        ),
        BlocProvider<LocationBloc>(
          create: (context) => LocationBloc()..add(LoadLocation()),
        ),
        BlocProvider<PeopleBloc>(
          create: (context) =>
              PeopleBloc(apiService: apiService)..add(LoadPeople()),
        ),
        BlocProvider<ProfileBloc>(create: (context) => ProfileBloc()),
        BlocProvider<ThemeCubit>(create: (context) => ThemeCubit()),
      ],
      child: BlocBuilder<ThemeCubit, ThemeMode>(
        builder: (context, themeMode) {
          return MaterialApp(
            title: 'Map My Friends',
            theme: AppTheme.lightTheme,
            darkTheme: AppTheme.darkTheme,
            themeMode: themeMode,
            home: const AuthWrapper(),
          );
        },
      ),
    );
  }
}

class AuthWrapper extends StatelessWidget {
  const AuthWrapper({super.key});

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<AuthBloc, AuthState>(
      builder: (context, state) {
        if (state is AuthInitial || state is AuthLoading) {
          return const Scaffold(
            body: Center(child: CircularProgressIndicator()),
          );
        }

        if (state is Authenticated) {
          return const MainScreen();
        }

        return const LoginScreen();
      },
    );
  }
}

class MainScreen extends StatefulWidget {
  const MainScreen({super.key});

  @override
  State<MainScreen> createState() => _MainScreenState();
}

class _MainScreenState extends State<MainScreen> {
  int _selectedIndex = 0;

  Widget _getScreen(int index) {
    switch (index) {
      case 0:
        return const MapScreen();
      case 1:
        return const PeopleScreen();
      case 2:
        return const MeScreen();
      default:
        return const MapScreen();
    }
  }

  void _onItemTapped(int index) {
    setState(() {
      _selectedIndex = index;
    });
  }

  void _logout() {
    context.read<AuthBloc>().add(LogoutRequested());
  }

  @override
  Widget build(BuildContext context) {
    return LayoutBuilder(
      builder: (context, constraints) {
        final isDesktop = constraints.maxWidth >= 600;

        return Scaffold(
          extendBodyBehindAppBar: true,
          // Removed AppBar as requested
          body: Stack(
            children: [
              // Content Layer
              Positioned.fill(child: _getScreen(_selectedIndex)),

              // Mobile Floating Logout Button (Top Right)
              if (!isDesktop)
                Positioned(
                  top: 50, // Safe area padding
                  right: 20,
                  child: GlassContainer(
                    padding: EdgeInsets.zero,
                    borderRadius: 30,
                    child: IconButton(
                      icon: const Icon(Icons.logout, color: Colors.indigo),
                      onPressed: _logout,
                      tooltip: 'Logout',
                    ),
                  ),
                ),

              // Glass Navigation Rail (Desktop)
              if (isDesktop)
                Positioned(
                  left: 20,
                  top: 20,
                  bottom: 20,
                  child: GlassContainer(
                    width: 80,
                    padding: const EdgeInsets.symmetric(vertical: 20),
                    child: Column(
                      children: [
                        const SizedBox(height: 20),
                        // App Logo or Icon could go here
                        const Icon(
                          Icons.map_outlined,
                          size: 32,
                          color: Colors.indigo,
                        ),
                        const SizedBox(height: 40),

                        _buildGlassNavItem(
                          icon: Icons.map_outlined,
                          selectedIcon: Icons.map,
                          label: 'Map',
                          index: 0,
                          isSelected: _selectedIndex == 0,
                        ),
                        const SizedBox(height: 20),
                        _buildGlassNavItem(
                          icon: Icons.people_outline,
                          selectedIcon: Icons.people,
                          label: 'People',
                          index: 1,
                          isSelected: _selectedIndex == 1,
                        ),
                        const SizedBox(height: 20),
                        _buildGlassNavItem(
                          icon: Icons.person_outline,
                          selectedIcon: Icons.person,
                          label: 'Me',
                          index: 2,
                          isSelected: _selectedIndex == 2,
                        ),

                        const Spacer(),
                        InkWell(
                          onTap: _logout,
                          child: Container(
                            width: 60,
                            padding: const EdgeInsets.symmetric(vertical: 8),
                            child: Column(
                              children: [
                                const Icon(
                                  Icons.logout,
                                  color: Colors.indigo,
                                  size: 28,
                                ),
                                const SizedBox(height: 4),
                                Text(
                                  'Logout',
                                  style: TextStyle(
                                    fontSize: 10,
                                    fontWeight: FontWeight.normal,
                                    color: Colors.grey[700],
                                  ),
                                ),
                              ],
                            ),
                          ),
                        ),
                        const SizedBox(height: 10),
                      ],
                    ),
                  ),
                ),

              // Glass Bottom Navigation (Mobile)
              if (!isDesktop)
                Positioned(
                  left: 20,
                  right: 20,
                  bottom: 20,
                  child: GlassContainer(
                    padding: const EdgeInsets.symmetric(
                      vertical: 10,
                      horizontal: 20,
                    ),
                    borderRadius: 30,
                    child: Row(
                      mainAxisAlignment: MainAxisAlignment.spaceAround,
                      children: [
                        _buildGlassNavItemMobile(
                          icon: Icons.map,
                          label: 'Map',
                          index: 0,
                          isSelected: _selectedIndex == 0,
                        ),
                        _buildGlassNavItemMobile(
                          icon: Icons.people,
                          label: 'People',
                          index: 1,
                          isSelected: _selectedIndex == 1,
                        ),
                        _buildGlassNavItemMobile(
                          icon: Icons.person,
                          label: 'Me',
                          index: 2,
                          isSelected: _selectedIndex == 2,
                        ),
                      ],
                    ),
                  ),
                ),
            ],
          ),
        );
      },
    );
  }

  Widget _buildGlassNavItem({
    required IconData icon,
    required IconData selectedIcon,
    required String label,
    required int index,
    required bool isSelected,
  }) {
    return InkWell(
      onTap: () => _onItemTapped(index),
      child: Container(
        width: 60,
        padding: const EdgeInsets.symmetric(vertical: 8),
        decoration: isSelected
            ? BoxDecoration(
                color: Colors.white.withOpacity(0.2),
                borderRadius: BorderRadius.circular(12),
              )
            : null,
        child: Column(
          children: [
            Icon(
              isSelected ? selectedIcon : icon,
              color: isSelected ? Colors.indigo : Colors.grey[700],
              size: 28,
            ),
            const SizedBox(height: 4),
            Text(
              label,
              style: TextStyle(
                fontSize: 10,
                fontWeight: isSelected ? FontWeight.bold : FontWeight.normal,
                color: isSelected ? Colors.indigo : Colors.grey[700],
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildGlassNavItemMobile({
    required IconData icon,
    required String label,
    required int index,
    required bool isSelected,
  }) {
    return InkWell(
      onTap: () => _onItemTapped(index),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          Icon(
            icon,
            color: isSelected ? Colors.indigo : Colors.grey[700],
            size: 28,
          ),
          Text(
            label,
            style: TextStyle(
              fontSize: 12,
              fontWeight: isSelected ? FontWeight.bold : FontWeight.normal,
              color: isSelected ? Colors.indigo : Colors.grey[700],
            ),
          ),
        ],
      ),
    );
  }
}
